package org.geonetwork.gaap.domain.group;

/**
 * Enum with uuids for default groups
 *
 */
public enum GroupUuid {
    ALL("all"), INTRANET("intranet"), GUEST("GUEST"), SAMPLE("sample");

    String uuid;

    GroupUuid(String uuid) {
        this.uuid = uuid;
    }

    public String toString() {
        return uuid;
    }
}
