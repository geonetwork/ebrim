package org.geonetwork.domain.ebrim.informationmodel.provenance;

import org.geonetwork.domain.ebrim.informationmodel.core.datatype.ShortName;
import org.geonetwork.domain.ebrim.informationmodel.core.datatype.String32;
import org.hibernate.search.annotations.DocumentId;
import org.hibernate.search.annotations.Indexed;
import org.hibernate.search.annotations.IndexedEmbedded;

/**
 * 
 * @author Heikki Doeleman
 * 
 */

@Indexed
public class EmailAddress {

	// for Hibernate

	@DocumentId
	private long id;

	@IndexedEmbedded
	private ShortName address;
	// private ObjectRef type;

	@IndexedEmbedded
	private String32 type;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((address == null) ? 0 : address.hashCode());
		result = prime * result + ((type == null) ? 0 : type.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		EmailAddress other = (EmailAddress) obj;
		if (address == null) {
			if (other.address != null)
				return false;
		} else if (!address.equals(other.address))
			return false;
		if (type == null) {
			if (other.type != null)
				return false;
		} else if (!type.equals(other.type))
			return false;
		return true;
	}

	public ShortName getAddress() {
		return address;
	}

	public void setAddress(ShortName address) {
		this.address = address;
	}

	public String32 getType() {
		return type;
	}

	public void setType(String32 type) {
		this.type = type;
	}

}
