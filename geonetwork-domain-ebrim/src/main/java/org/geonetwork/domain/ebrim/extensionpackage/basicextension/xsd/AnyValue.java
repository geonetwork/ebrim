package org.geonetwork.domain.ebrim.extensionpackage.basicextension.xsd;

import org.hibernate.search.annotations.Indexed;
import org.hibernate.search.annotations.IndexedEmbedded;

/**
 * 
 * Currently this class can only hold an object of type gml:Envelope.
 * 
 * @author heikki doeleman
 *
 */
@Indexed
public class AnyValue {

	@IndexedEmbedded
	private Envelope value ;

	public Envelope getValue() {
		return value;
	}

	public void setValue(Envelope value) {
		this.value = value;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((value == null) ? 0 : value.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		AnyValue other = (AnyValue) obj;
		if (value == null) {
			if (other.value != null)
				return false;
		} else if (!value.equals(other.value))
			return false;
		return true;
	}



}
