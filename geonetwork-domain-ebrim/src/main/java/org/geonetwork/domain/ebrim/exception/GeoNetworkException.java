package org.geonetwork.domain.ebrim.exception;

/**
 * GeoNetworkException is a CheckedException.
 * 
 * See also http://trac.osgeo.org/geonetwork/wiki/GeoNetworkException
 * 
 * @author Erik van Ingen
 */

public class GeoNetworkException extends Exception {

	private static final long serialVersionUID = -1219050873452746605L;

	public GeoNetworkException(Throwable e) {
		super(e);
	}

	public GeoNetworkException(String newMessage) {
		super(newMessage);
	}

	public GeoNetworkException(String msg, Throwable e) {
		super(msg, e);
	}

}
